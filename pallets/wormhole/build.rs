//! Build script for pallet-wormhole.
//!
//! Generates circuit binaries (private_batch_*.bin, public_batch_*.bin) at build time.
//! This ensures the binaries are always consistent with the circuit crate version and
//! eliminates the need to commit large binary files to the repository.
//!
//! Note: Circuit generation cannot be skipped for this pallet because the binaries are
//! embedded at compile time via `include_bytes!`.

use std::{env, fs, path::Path, time::Instant};

/// Compute Poseidon2 hash of bytes and return hex string
fn poseidon_hex(data: &[u8]) -> String {
	let hash = qp_poseidon_core::hash_bytes(data);
	hex::encode(&hash[..16]) // first 16 bytes for shorter display
}

/// Print hash of a generated binary file
fn print_bin_hash(dir: &Path, filename: &str) {
	let path = dir.join(filename);
	if let Ok(data) = std::fs::read(&path) {
		println!(
			"cargo:warning=  {}: {} bytes, hash: {}",
			filename,
			data.len(),
			poseidon_hex(&data)
		);
	}
}

fn main() {
	let out_dir = env::var("OUT_DIR").expect("OUT_DIR not set");
	// Default to 7 leaves: fits in degree_bits=15 (~1.5 GB peak memory for mobile).
	// 8+ leaves require degree_bits=16 (~2.5 GB peak), limiting to 6GB+ devices.
	let num_leaf_proofs: usize = env::var("QP_NUM_LEAF_PROOFS")
		.unwrap_or_else(|_| "7".to_string())
		.parse()
		.expect("QP_NUM_LEAF_PROOFS must be a valid usize");

	// Default to 4 inner private batches per public batch.
	let num_private_batch_proofs: usize = env::var("QP_NUM_PRIVATE_BATCH_PROOFS")
		.unwrap_or_else(|_| "4".to_string())
		.parse()
		.expect("QP_NUM_PRIVATE_BATCH_PROOFS must be a valid usize");

	// cargo:warning= messages are shown during build when the script runs
	println!(
		"cargo:warning=[pallet-wormhole] Generating ZK circuit binaries (num_leaf_proofs={}, num_private_batch_proofs={})...",
		num_leaf_proofs, num_private_batch_proofs
	);

	let start = Instant::now();

	// Generate leaf + private batch + public batch verifier binaries (no prover).
	qp_wormhole_circuit_builder::generate_all_circuit_binaries(
		Path::new(&out_dir),
		false, // include_prover = false
		num_leaf_proofs,
		Some(num_private_batch_proofs),
	)
	.expect("Failed to generate circuit binaries");

	let elapsed = start.elapsed();
	println!(
		"cargo:warning=[pallet-wormhole] ZK circuit binaries generated in {:.2}s",
		elapsed.as_secs_f64()
	);

	// Embed circuit sizing constants for PI parsing at runtime.
	let config_rs = format!(
		"// Auto-generated by build.rs — do not edit.\n\
		 pub const NUM_LEAF_PROOFS: usize = {num_leaf_proofs};\n\
		 pub const NUM_PRIVATE_BATCH_PROOFS: usize = {num_private_batch_proofs};\n"
	);
	fs::write(Path::new(&out_dir).join("wormhole_circuit_config.rs"), config_rs)
		.expect("Failed to write wormhole_circuit_config.rs");

	// Print hashes of generated binaries
	let out_path = Path::new(&out_dir);
	print_bin_hash(out_path, "common.bin");
	print_bin_hash(out_path, "verifier.bin");
	print_bin_hash(out_path, "dummy_proof.bin");
	print_bin_hash(out_path, "private_batch_common.bin");
	print_bin_hash(out_path, "private_batch_verifier.bin");
	print_bin_hash(out_path, "public_batch_common.bin");
	print_bin_hash(out_path, "public_batch_verifier.bin");
}
